    /**
     * 根据位置得到对应的Fragment
     * @return
     */
    private BaseFragment getFragment() {
        BaseFragment fragment = mBaseFragment.get(position);
        return fragment;
    }

    private void initFragment() {
        mBaseFragment = new ArrayList<>();
        mBaseFragment.add(new CustomFragment());//首页Fragment
        mBaseFragment.add(new FindPartyFragment());//发现Fragment
        mBaseFragment.add(new MarketFragment());//市场Fragment
        mBaseFragment.add(new MyAssetsFragment());//我的资产Fragment
        mBaseFragment.add(new SettingFragment());//设置Fragment
    }

    private void initView() {
        setContentView(R.layout.activity_main);
        mRg_main = (RadioGroup) findViewById(R.id.rg_main);

    }
